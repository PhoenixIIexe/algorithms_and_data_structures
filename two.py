x_1 = int(input('Введите координаты точки x первой: '))
y_1 = int(input('Введите координаты точки y первой: '))
x_2 = int(input('Введите координаты точки x второй: '))
y_2 = int(input('Введите координаты точки y второй: '))

if x_1 != x_2:
    k = (y_1-y_2)/(x_1-x_2)
    b = (x_1*y_2 - x_2*y_1)/(x_1-x_2)
    print(f'y = {k}x + {b}')
else:
    print('y - любое число')